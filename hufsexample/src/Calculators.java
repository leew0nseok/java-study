public class Calculators {
    static int max(int a, int b){
        if(a > b) return a;
        else return b;
    }

    static int min(int a, int b){
        if(a < b) return a;
        else return b;
    }

    static int abs(int i) {
        if (i < 0) return -i;
        else return i;
    }
}
